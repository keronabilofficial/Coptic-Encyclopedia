export function convertLegacyToUnicodeCoptic(legacyText) {
  if (!legacyText) return '';
  
  const copticMap = {
    // الحروف القبطية الخاصة المقابلة للرموز والأرقام
    '2': 'ϣ', 'q': 'Ϧ', 'Q': 'Ϧ', '4': 'ϥ', 'h': 'ϩ', 'H': 'Ϩ',
    'g': 'ϫ', 'G': 'Ϫ', 's': 'ϭ', 'S': 'Ϭ', '5': 'ϯ', '3': 'ⲏ',
    '0': 'ⲑ', '0': 'Ⲑ', 'w': 'ⲱ', 'W': 'Ⲱ', 'x': 'ⲭ', 'X': 'Ⲭ',
    'j': 'ⲅ', 'J': 'Ⲅ', 'f': 'ⲫ', 'F': 'Ⲩ', 'y': 'ⲯ', 'Y': 'Ⲯ',
    '`': '̀', // الجنكم
    
    // الحروف العادية
    'a': 'ⲁ', 'A': 'Ⲁ', 'b': 'ⲃ', 'B': 'Ⲃ', 'e': 'ⲉ', 'E': 'Ⲉ',
    'z': 'ⲍ', 'Z': 'Ⲋ', 'i': 'ⲓ', 'I': 'Ⲓ', 'k': 'ⲕ', 'K': 'Ⲕ',
    'l': 'ⲗ', 'L': 'Ⲗ', 'm': 'ⲙ', 'M': 'Ⲙ', 'n': 'ⲛ', 'N': 'Ⲛ',
    'o': 'ⲟ', 'O': 'Ⲟ', 'p': 'ⲡ', 'P': 'Ⲡ', 'r': 'ⲣ', 'R': 'Ⲣ',
    'c': 'ⲥ', 'C': 'Ⲥ', 't': 'ⲧ', 'T': 'Ⲧ', 'v': 'ⲩ', 'V': 'Ⲩ',
    
    // تنظيف الحشو والرموز الزائدة الناتجة عن تنسيقات الكتب القديمة
    '\\': '', '%': '', '=': '', '(': ''
  };

  return legacyText
    .split('')
    .map(char => copticMap[char] !== undefined ? copticMap[char] : char)
    .join('')
    // ضبط مكان الجنكم تلقائياً ليظهر فوق الحرف بشكل صحيح في اليونيكود الحديث
    .replace(/̀([ⲁ-ⲱϣ-ϯ])/g, '$1̀');
}